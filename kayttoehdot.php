<h1>Käyttöehdot</h1>

<p><i>7.9.2013</i></p>

<p>NääsPeksin yhteystietojärjestelmän henkilörekisterin tarkoitus on sujuvoittaa isokokoisen ja alati vaihtuvan teatteriproduktion tekijätiimin hallintaa sijoittamalla yleisimmät henkilötietoja vaativat toiminnot yhteen paikkaan. Näihin kuuluvat esimerkiksi yhteisiin tapahtumiin ja eri osa-alueisiin ilmoittautuminen sekä näytelmän tekemiseen liittyvien tuotteiden tilaaminen.</p>

<p>Järjestelmän käyttäminen on produktion kannalta jokaiselle tekijälle pakollista. Omat tiedot kukin voi silti täyttää oman harkintansa mukaan tiedostaen, että puutteellisista tiedoista seuraavat ongelmat ovat tällöin omalla vastuulla. Käyttäjän nimeä lukuunottamatta mitkään tiedot eivät näy järjestelmän ulkopuolelle ja sisäpuolellakin näytetään vain kontekstin kannalta olennaiset tiedot. Mitään luottamuksellista järjestelmään ei kuitenkaan kannata uskoa.</p>

<p>Järjestelmään rekisteröityminen ja kirjautuminen on mahdollista täsmälleen niille, jotka tietävät tekijätiimin yhteisen salasanan. Tästä syystä salasanaa ei tule luovuttaa ulkopuolisille. Käyttäjä on vapaa halutessaan poistamaan omat tietonsa ja poistumaan tekijätiimistä, mutta siitä kannattaa ilmoittaa myös erikseen tuottajille sekä omien osa-alueiden vetäjille. Luonnollisesti tämä ei vapauta sitovista tilauksista.</p>

<p>Pääsyä henkilötietoihin ei missään nimessä luovuteta kolmannelle osapuolelle. Poikkeuksena edelliseen ovat ilmeiset käyttötarkoitukseen sovelletut tapaukset, kuten nimi- ja allergialistojen luovuttaminen ravintoloille yhteisissä ruokailuissa.</p>

